#wpp to perform indexing, slicing & step size on given string

st1="yasham acedemy"

 #indexing
#(+ve) indexing
print(st1[0])


#(-ve) indexing
print(st1[-14])

#slicing
#(+ve)slicing
print(st1[0:6])

#(-ve)slicing
print(st1[-14:-8])

#(+ve)step size
print(st1[0:13:2])

#(-ve)step size
print(st1[-14:-1:3])

print(st1[-1:-14 -3])

#reversing the string
print(st1[::-1])


